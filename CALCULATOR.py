san1 = float(input())
san2 = float(input())
amal = input()

if san2 == 0 and amal == "/":
    print("888888")

elif amal == "+":
    print(san1 + san2)
elif amal == "-":
    print(san1 - san2)
elif amal == "*":
    print(san1 * san2)
elif amal == "/":
    print(san1 / san2)

else:
    print("888888")